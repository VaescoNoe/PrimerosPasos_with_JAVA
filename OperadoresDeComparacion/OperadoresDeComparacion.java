public class OperadoresDeComparacion{
    public static void main(String [] args){
        double sueldo1 = 10000.2552;
        double sueldo2 = 452165.21;
        System.out.println(sueldo1 > sueldo2);
        System.out.println(sueldo1 < sueldo2);
        System.out.println(sueldo1 >= sueldo2);
        System.out.println(sueldo1 <= sueldo2);
        System.out.println(sueldo1 != sueldo2);
        System.out.println(sueldo1 == sueldo2);
    }
}